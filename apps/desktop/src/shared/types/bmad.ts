/**
 * BMad Studio shared types
 * ========================
 *
 * Types and Zod schemas used across main / preload / renderer for the BMad
 * Studio engine-swap subsystem. Every CSV/YAML/TOML parse, every IPC payload,
 * and every external CLI output that touches the renderer goes through one of
 * the schemas in this file.
 *
 * Keep this file 100% framework-free: no Electron imports, no Node-only APIs.
 * The schemas need to be importable from the renderer for client-side
 * validation of cached IPC payloads.
 *
 * Doc citations live next to the type they ground:
 *   per BMAD docs § "What got installed" — manifest.yaml shape
 *   per BMAD docs § "Three-Layer Override Model" — customization layers
 *   per BMAD docs § "Merge Rules (by shape, not by field name)" — resolver semantics
 *   per `bmad-help/SKILL.md` § "CSV Interpretation" — bmad-help.csv schema
 *
 * See: apps/desktop/docs/bmad-migration/INVENTORY.md (§3 phase graph) +
 *      apps/desktop/docs/bmad-migration/DECISIONS.md (KAD-1..10 + D-NN)
 */


export const BmadErrorSchema = z.object({
  code: z.enum(BMAD_ERROR_CODES),
  message: z.string().min(1),
  details: z.unknown().optional(),
});

export type BmadIpcResult<T = unknown> =
  | { readonly success: true; readonly data: T }
  | { readonly success: false; readonly error: BmadError };

/**
 * Construct an "ok" envelope. Inline-friendly for handler bodies.
 */
export function bmadOk<T>(data: T): BmadIpcResult<T> {
  return { success: true, data };
}

/**
 * Construct a structured-error envelope. `details` may be any JSON-serializable
 * value (Zod issue list, raw stderr, etc.) — the renderer treats it as opaque
 * data attached to the error code.
 */
export function bmadFail<T = never>(
  code: BmadErrorCode,
  message: string,
  details?: unknown,
): BmadIpcResult<T> {
  return { success: false, error: { code, message, details } };
}

